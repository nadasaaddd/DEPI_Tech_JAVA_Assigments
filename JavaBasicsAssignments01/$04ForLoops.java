package JavaBasicsAssignments01;

public class $04ForLoops {
    public static void main(String[] args) {
        int baseNumber = 7;
        for (int i = 1; i <= 10; i++) {
            int pro = baseNumber * i;
            System.out.println(baseNumber + " x " + i + " = " + pro);
        }
    }
}
